    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = this.tblDatos.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una sucursal",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Obtener los datos de la sucursal que se va a editar
        try{
            SucursalesDTO s 
                    = (SucursalesDTO)this.tblDatos.getValueAt(row, SucursalesModel.COL_OBJECT);
            //Inicializar el formulario en modo EdiciÃ³n
            WinSucursalesForm sucursalesForm = 
                new WinSucursalesForm(this, s, false);
            //Hacer visible el formulario
            sucursalesForm.setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error: "+e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = this.tblDatos.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una sucursal",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Integer idSucursal =
                Integer.parseInt(this.tblDatos.getValueAt(row, 
                        SucursalesModel.COL_ID_SUCURSAL).toString());
        try{
            sucursalesDao.eliminarSucursal(idSucursal);
            this.consultarSucursales();
        }catch(Exception e){
           JOptionPane.showMessageDialog(this, e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

