    public void consultarSucursales(){
        try{
            //Obtener lista de sucursales desde BD
            List<SucursalesDTO> lstSucursales = 
                    sucursalesDao.consultarSucursales(null, null);
            //Crear el modelo para dibujar la tabla de sucursales
            SucursalesModel model = new SucursalesModel(lstSucursales);
            //Relacionar el modelo con la tabla del formulario
            this.tblDatos.setModel(model);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error "+e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
